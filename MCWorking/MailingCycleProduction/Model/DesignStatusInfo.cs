using System;
using System.Collections.Generic;
using System.Text;
using System.Drawing;

namespace Irmac.MailingCycle.Model
{
    /// <summary>
    /// Business Entity used to model Design Statuses.
    /// </summary>
    [Serializable]
    public class DesignStatusInfo : BaseInfo
    {
        private int agentId = 0;
        private string agentName = string.Empty;
        private string phone = string.Empty;
        private string category = string.Empty;
        private string status = string.Empty;
        private string lastModifyDate = string.Empty;
        private int daysInStatus = 0;
        
        /// <summary>
        /// Default constructor.
        /// </summary>
        public DesignStatusInfo()
        {
            //
        }

        public int AgentId
        {
            get
            {
                return agentId;
            }
            set
            {
                agentId = value;
            }
        }

        public string AgentName
        {
            get
            {
                return agentName;
            }
            set
            {
                agentName = value;
            }
        }

        public string Phone
        {
            get
            {
                return phone;
            }
            set
            {
                phone = value;
            }
        }

        public string Category
        {
            get
            {
                return category;
            }
            set
            {
                category = value;
            }
        }

        public string Status
        {
            get
            {
                return status;
            }
            set
            {
                status = value;
            }
        }

        public string LastModifyDate
        {
            get
            {
                return lastModifyDate;
            }
            set
            {
                lastModifyDate = value;
            }
        }

        public int DaysInStatus
        {
            get
            {
                return daysInStatus;
            }
            set
            {
                daysInStatus = value;
            }
        }
    }
}
